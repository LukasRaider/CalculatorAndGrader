package CalculatorAndGrader;

public class SimpleCalculator {

    public int Addition (int numberA, int numberB){
        return numberA + numberB;
    }
    public int Subtraction(int numberA, int numberB){
        return numberA - numberB;
    }
    public int Multiplication(int numberA, int numberB){
        return numberA * numberB;
    }
    public int Division(int numberA, int numberB){return numberA / numberB;}
    public int Empowerment(int numberA){
        return numberA * numberA;
    }

}
